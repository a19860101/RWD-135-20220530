//條件式

// if...
let x = -10;
if(x > 0){
    console.log('success');
}

// if...else
if(x > 0){
    console.log('success 2');
}else{
    console.log('error 2');
}

// if...else if
if(x > 0){
    console.log('正數');
}else if(x < 0){
    console.log('負數');
}

// if..else if...else
if(x > 0){
    console.log('正');
}else if(x < 0){
    console.log('負');
}else{
    console.log(0)
}